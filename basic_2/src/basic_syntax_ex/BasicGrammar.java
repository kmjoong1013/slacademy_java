package basic_syntax_ex;

public class BasicGrammar {
	
	public static void main (String[] args) {
		
		int 국어=80;
		int English=90;
		int 數學=75;
		
		int total = 0;
		
		total = 국어 + English + 數學;
		
		System.out.println("국어 : "+국어);
		System.out.println("English : "+English);
		System.out.println("數學 : "+數學);
		System.out.println("총점 : " +total);
		
		float f = 3.14e03f;
		
		System.out.println("f: " +f);
	}
}
