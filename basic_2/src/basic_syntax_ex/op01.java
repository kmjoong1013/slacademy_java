package basic_syntax_ex;

public class op01 {

	public static void main(String[] args) {
		int a=0, b=0;
		System.out.println(" a++ =" + a++);
		System.out.println(" ++b =" + ++b);
		
		System.out.println(" a++, ++b실행 이후의 값 =" );
		System.out.println(" a =" + a);
		System.out.println(" b =" + b);
	}
}
